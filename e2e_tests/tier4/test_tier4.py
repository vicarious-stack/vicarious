    html_path = os.path.join(PROJECT_ROOT, "index.html")
    if not os.path.exists(html_path):
        raise unittest.SkipTest("index.html is missing")
    root, style, js = load_index_html(html_path)
    
    # Ensure there is a content container element (e.g. div with class/id content or reader)
    content_containers = root.find_all(class_name="content") + root.find_all(class_name="reader") + root.find_all("main")
    if not content_containers and not root.find_by_id("content") and not root.find_by_id("reader"):
        raise AssertionError("index.html is missing main content rendering container (#content, .content, or <main>)")
