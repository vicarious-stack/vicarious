def test_t4_complete_assembled_book_verification():
    """122. Run build scripts to compile the_vicarious_complete.md, count words, run validations, and check that the resulting markdown is loaded and parsed correctly in the reader."""
    assemble_path = os.path.join(PROJECT_ROOT, "assemble.py")
    if not os.path.exists(assemble_path):
        raise unittest.SkipTest("assemble.py is missing")
    
    # Run assemble.py
    code, out, err = run_cli_tool([sys.executable, "assemble.py"], cwd=PROJECT_ROOT)
    if code != 0:
        raise AssertionError(f"assemble.py failed to run. Stderr: {err}")
    
    compiled_path = os.path.join(PROJECT_ROOT, "the_vicarious_complete.md")
    if not os.path.exists(compiled_path):
        raise AssertionError("the_vicarious_complete.md was not generated by assemble.py")
        
    with open(compiled_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    struct = parse_markdown_structure(content)
    if struct["word_count"] < 100:
        raise AssertionError("Assembled book word count is unexpectedly low, compilation might have failed")

